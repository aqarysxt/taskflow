/**
 * Profile Avatar — renders a person silhouette SVG icon inside a gradient circle
 * Replaces letter-based "smiley" avatars with a proper profile icon.
 */
export function ProfileAvatar({ name = '', role = 'mvp', size = 36, className = '', style = {} }) {
  const isAdmin = role === 'admin';
  const gradients = {
    admin:   ['#f59e0b', '#d97706'],
    mvp:     ['#3b82f6', '#1d4ed8'],
    alice:   ['#8b5cf6', '#6d28d9'],
    bob:     ['#10b981', '#059669'],
    charlie: ['#ec4899', '#be185d'],
  };
  // Pick gradient based on name for variety
  const firstName = name.split(' ')[0]?.toLowerCase() || 'mvp';
  const g = gradients[firstName] || (isAdmin ? gradients.admin : gradients.mvp);

  const uid = `grad-${name.replace(/\s/g, '')}-${size}`;

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 40 40"
      className={`profile-avatar ${className}`}
      style={{ flexShrink: 0, borderRadius: '50%', ...style }}
    >
      <defs>
        <linearGradient id={uid} x1="0%" y1="0%" x2="100%" y2="100%">
          <stop offset="0%" stopColor={g[0]} />
          <stop offset="100%" stopColor={g[1]} />
        </linearGradient>
        <clipPath id={`clip-${uid}`}>
          <circle cx="20" cy="20" r="20" />
        </clipPath>
      </defs>
      <circle cx="20" cy="20" r="20" fill={`url(#${uid})`} />
      {/* Head */}
      <circle cx="20" cy="15" r="7" fill="rgba(255,255,255,0.9)" clipPath={`url(#clip-${uid})`} />
      {/* Body */}
      <ellipse cx="20" cy="32" rx="11" ry="9" fill="rgba(255,255,255,0.9)" clipPath={`url(#clip-${uid})`} />
    </svg>
  );
}

